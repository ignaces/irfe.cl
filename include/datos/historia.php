<div id="texto">
<p>Colegio fundado en 1940, por la Fundación Centro Cristiano, entidad que tiene entre sus fines promover y fomentar la Educación Católica de la Juventud. La Fundación entrega a los Padres Pallottinos Schoenstattianos la responsabilidad  de construir y administrar esta casa de estudios, siendo su primer Rector el Padre José Kuhl.</p>
<p>En 1948 visita el colegio el Padre José Kentenich quien coloca la primera piedra del Santuario de Schoenstatt en Santa Cruz, su homilía, en esa ceremonia, es un verdadero legado inspirador del Proyecto Educativo del Colegio.</p>
<br />
<p>El Instituto Regional Federico Errázuriz tiene sus raíces en el espíritu de los Padres fundadores que venidos de la ciudad de Limburgo en Alemania, imprimieron su impronta pedagógica y d excelencia que se mantiene y se mantendrá en el quehacer de esta prestigiosa Institución.</p>
<br />
<ul>Se mantiene desde el origen como un verdadero estilo Institutano, entre otras las siguientes características:</ul> 
<br />
<li>Un edificio de gran belleza arquitectónica.</li>
<li>Enseñanza que conjuga alta exigencia Académica y sólida Formación Cristiana.</li>
<li>Colores institucionales el rojo, negro y amarillo que están en el edificio, estandarte, bandera, insignia y otros símbolos.</li>
<li>Himno del colegio creado por el Padre José Kulh.</li>
<li>Banda de guerra que acompaña los desfiles institucionales.</li>
<li>Sólidos resultados académicos y proyección de sus alumnos a Educación Universitaria.</li>
<br />
<p>En 1968 los Padres Pallotlinos se retiran de Chile y el colegio vuelve a la Fundación Centro Cristiano que lo administra en la actualidad. Imparte Educación Pre-Básica, Básica y Enseñanza Media con una matricula de 1.753 alumnos. Una de sus fortalezas es el fomento del uso del Tiempo Libre y el desarrollo de los intereses de los alumnos para lo cual ofrece 37 acles en la áreas de deporte, artística, científico-tecnológica y cívico-social.</p>
<br />
<p>El lema del colegio es: <strong>“Dios y Patria”</strong>.</p>
